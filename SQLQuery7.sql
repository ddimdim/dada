/****** Скрипт для команды SelectTopNRows из среды SSMS  ******/
SELECT TOP (1000) [Номер товара]
      ,[Наименование товара]
      ,[Единица измерения товара]
      ,[Цена закупки]
  FROM [Оптовые_продажи_Шурков].[dbo].[Товары]
  Select * From [Товары] ORDER BY [Наименование товара]
  SELECT AVG([Цена закупки]) AS 'Средняя цена' From [Товары]