/****** Скрипт для команды SelectTopNRows из среды SSMS  ******/
SELECT TOP (1000) [Номер товара]
      ,[Цена закупки]
      ,[Цена продажи]
      ,[Количество проданных единиц товара]
      ,[Дата продажи]
  FROM [Оптовые_продажи_Шурков].[dbo].[Продажи]
  Select *From [Продажи] order by [Номер товара] 
  Select MIN([Количество проданных единиц товара]) AS 'Наименьшее количество' FROM [Продажи]
   Select Max([Количество проданных единиц товара]) AS 'Наибольшее количество' FROM [Продажи]